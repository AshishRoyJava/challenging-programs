package problems.basic.programs;

/**
 * How to print different type of values in Java?
 * 
 * @author Mrityunjay 21-Sep-2018 3:17:57 PM
 *
 */
public class PrintDifferentValue {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int x = 10;
		float f = 12.5f;
		double d = 20.5d;
		String str = "Ram";
		System.out.println("Output: " + str);
		System.out.println("Output: " + x);
		System.out.println("Output: " + f);
		System.out.println("Output: " + d);
	}

}
